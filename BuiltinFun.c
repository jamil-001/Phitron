#include<stdio.h>
#include<math.h>
int main(){
    //ceil == upper value find;
    // floor == lowest value find;
    // round == nerest value find;

    double a = 10.5;
    printf("%lf\n",ceil(a));

    printf("%lf\n",floor(a));

    printf("%lf\n",round(a));

    printf("%lf\n",sqrt(20));

    printf("%lf\n",pow(3,2));

    return 0;
}